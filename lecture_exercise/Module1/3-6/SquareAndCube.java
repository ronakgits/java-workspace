public class SquareAndCube {
    
    public static void main(String[] args) {
        
        int num = 5, square, cube; 

        square = num * num; 
        cube = num * num * num; 

        System.out.println("Square of the number is : " + square);

        System.out.println("Cube of the number is : " + cube);

    }
}
